class Solution{
public:
    int findMin(int arr[], int n){
        //complete the function here
        
        map<int, int>mp;
        for(int i=0;i<n;i++)
        {
            mp[arr[i]]++;
        }
        for(auto itr:mp)
        {
            return itr.first;
        }
    }
};

